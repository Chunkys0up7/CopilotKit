#!/usr/bin/env bash
# =============================================================================
# install-bootstrap.sh — single-file CopilotKit project installer.
#
# This script has the entire kickstarter project layout embedded inside it
# as a base64-encoded tar.gz. Paste this whole file into your environment,
# run it, and you have a working project in seconds. No git clone, no
# GitHub access, nothing else needed.
#
# Usage:
#   bash install-bootstrap.sh                 # creates ./copilotkit-app/
#   bash install-bootstrap.sh /workspace/foo  # creates /workspace/foo/
#   bash install-bootstrap.sh ./bar --force   # overwrite an existing dir
#
# After the script runs:
#   cd <target>/backend && python -m venv .venv && source .venv/bin/activate
#   pip install -r requirements.txt && uvicorn app.main:app --reload --port 8000
#
#   cd <target>/frontend && npm install && npm run dev
#
#   open http://localhost:3000
#
# Total elapsed: ~5 seconds for the script + ~5 minutes for installs.
# =============================================================================
set -euo pipefail

target="${1:-./copilotkit-app}"
force=false
[[ "${2:-}" == "--force" ]] && force=true
[[ "${1:-}" == "--force" ]] && { target="./copilotkit-app"; force=true; }

if [[ -d "$target" ]] && [[ -n "$(ls -A "$target" 2>/dev/null)" ]] && [[ "$force" != true ]]; then
  echo "ERROR: $target is not empty. Use --force or pick an empty dir." >&2
  exit 1
fi

mkdir -p "$target"
target="$(cd "$target" && pwd)"

echo "==> Extracting CopilotKit kickstarter into $target"

# Decode + extract the embedded archive below.
base64 -d <<'PAYLOAD_END' | tar xzf - -C "$target"
