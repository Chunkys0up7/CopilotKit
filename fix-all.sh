#!/usr/bin/env bash
# =============================================================================
# fix-all.sh — patch an existing CopilotKit kickstarter scaffold to the
# latest main.
#
# What it does:
#   - Overlays the current source tree (backend/ frontend/ docs/ scripts/
#     plus root README/RUNBOOK/ARCHITECTURE/CHANGELOG/.gitignore/.env.example)
#     onto your existing project directory.
#   - Preserves your .env, .venv, node_modules, .git, package-lock.json,
#     and any files outside the source tree (they're not in the tarball,
#     so they aren't touched).
#   - Wipes frontend/.next so Next.js picks up next.config.js changes
#     (transpilePackages requires a clean cache).
#   - Prints a concise list of what to do next.
#
# What it does NOT do:
#   - Does not run pip install or npm install. After it finishes, you
#     decide if/when to re-resolve deps (your corporate mirror may
#     prefer caution).
#   - Does not restart your dev servers.
#   - Does not git commit your project's pending changes.
#
# Usage:
#   bash fix-all.sh                   # patches ./
#   bash fix-all.sh /workspace/foo    # patches /workspace/foo/
# =============================================================================
set -euo pipefail

target="${1:-.}"
[[ ! -d "$target" ]] && { echo "ERROR: $target is not a directory" >&2; exit 1; }
target="$(cd "$target" && pwd)"

# Sanity check: looks like a kickstarter project?
if [[ ! -f "$target/backend/app/main.py" ]] && [[ ! -f "$target/RUNBOOK.md" ]]; then
  echo "WARN: $target doesn't look like a CopilotKit kickstarter project" >&2
  echo "      (missing backend/app/main.py and RUNBOOK.md)" >&2
  read -p "      Continue anyway? [y/N] " ans
  [[ "$ans" != "y" && "$ans" != "Y" ]] && { echo "aborted"; exit 1; }
fi

echo "==> Patching CopilotKit kickstarter at $target"
echo "    (preserving .env, .venv, node_modules, .git, package-lock.json)"
echo

# Snapshot what was there
before=$(find "$target" -type f ! -path "*/.git/*" ! -path "*/.venv/*" ! -path "*/node_modules/*" ! -path "*/.next/*" 2>/dev/null | wc -l | tr -d ' ')
echo "    files before: $before"

# Decode + overlay. Tar overwrites only files that exist in the archive;
# anything in $target outside the source tree is left alone.
base64 -d <<'PAYLOAD_END' | tar xzf - -C "$target"
